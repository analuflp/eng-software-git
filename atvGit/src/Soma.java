public class Soma {

    private int numero1;
    private int numero2;
    private int resultado;


    public void somarDoisNumeros(int num1, int num2) {
        this.numero1 = num1;
        this.numero2 = num2;
        this.resultado = num1 + num2;
    }


    public int getResultado() {
        return resultado;
    }



}
